    public static List<String> readExcel(Context context) {
        return readFile(context, new String[]{".xlsx", ".xls"});
    }


    public static List<String> readFile(Context context, final String[] fileType) {

        List<String> paths = new ArrayList<>();
        StringBuilder selection = new StringBuilder();
        for (int i = 0; i < fileType.length; i++) {
            if (i != 0) {
                selection.append(" OR ");
            }
            selection.append(MediaStore.Files.FileColumns.DATA).append(" LIKE '%").append(fileType[i]).append("'");
        }
        ContentResolver resolver = context.getContentResolver();
        Cursor cursor = resolver.query(MediaStore.Files.getContentUri("external"),
                new String[]{MediaStore.Files.FileColumns.DATA},
                selection.toString(),
                null,
                null);

        if (cursor == null || cursor.getCount() == 0) {
            return null;
        }
        if (cursor.moveToLast()) {
            do {
                String data = cursor.getString(0);
                paths.add(data);
            } while (cursor.moveToPrevious());

        }
        cursor.close();
        return paths;
    }

